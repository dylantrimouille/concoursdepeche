            2. Pas d’inscription le jour du concours.<br>
            3. Aucun remboursement ne sera effectué en cas de désistement.<br>
            4. Tous les pêcheurs doivent être en possession d’une carte de pêche de l’année en cours.<br>
            5. Modalité de pêche :<br>
            a) Une canne par pêcheur.<br>
            b) Amorçage 12 litres maximum et esches 1 litre maximum.<br>
            c) Fouillis vers de vase interdit.<br>
            d) Moulinet interdit.<br>
            6. Deux bourriches obligatoires ou 1 bourriche et un seau obligatoire (tri des poissons chats
            obligatoire).<br>
            7. Tous les poissons comptent. Carpe de plus de 1 kg = 1000 points.<br>
            8. Chaque compétiteur est responsable de sa ligne et se doit de toujours l’avoir sous surveillance.<br>
            9. La compétition sous tous ses aspects (organisation et sportif) est réglementée par le présent
            document. Il devra être lu et approuvé par tous les concurrents.<br>
            10. L’AAPPMA de Moulins se réserve le droit d’imposer des modalités particulières dans le
            déroulement du concours en fonction des conditions sanitaires et des prérogatives
            gouvernementales. De même, l’AAPPMA de Moulins se réserve le droit d’annuler le concours pour
            des raisons liées à la sécurité des participants. Les concurrents seraient alors remboursés de leur
            inscription.<br>
            ORGANISATION :
            1. Lieu de la compétition : Plan d’eau à définir.<br>
            2. Le tirage des postes se fera dans l’ordre d’arrivée des fiches d’inscription.<br>
            3. Déroulement :<br>
            ➢ Rassemblement : 8h00<br>
            ➢ Tirage des postes et installation : 8h30<br>
            ➢ Amorçage : 9h50<br>
            ➢ Début du concours : 10h00<br>
            ➢ Fin du concours : 15h30<br>
            ➢ Classement et remise des lots : 17h00<br>
            POINTS ET CLASSEMENTS :<br>
            1. Le classement se fait au poids total des prises de chaque pêcheur et par secteur.<br>
            2. Le vainqueur sera le compétiteur qui aura obtenu le poids le plus élevé.<br>
            3. Les exæquos seront traités suivant l’ordre des arrivées des fiches d’inscription.<br>
